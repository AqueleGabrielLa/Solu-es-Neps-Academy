#include <stdio.h>
int main(){
    int vetor[10];
    int i;
    for(i=0; i<10; i++){
        scanf("%d", &vetor[i]);
    }
    if(vetor[i] = vetor[10]){
        for(i = 10; i >= 1; i--){
            printf("%d\n", vetor[i - 1]);   
        }
    }

    return 0;
}